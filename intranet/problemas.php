				</TR>
				<TR CLASS="contenido"> 
					<TD VALIGN="TOP">R:</TD>
							<TD><P>Este problema sucede cuando tu no figuras en nuestros registros como integrante activo 
									dentro de la Escuela IC, ya sea como acad&eacute;mico, administrativo, alumno, integrante 
									del CEC o webmaster. Para solucionar esto:</P>
								<UL>
									<LI>Si eres alumno o integrante del CEC, entonces debes tener inscrita la boleta de &quot;Inscripci&oacute;n 
										de Asignaturas&quot;.</LI>
									<LI>Si eres acad&eacute;mico, administrativo o webmaster, entonces debe existir una constancia 
										escrita en la Escuela IC, o el Area de Computaci&oacute;n de tu existencia y de tus datos.</LI>
								</UL></TD>
