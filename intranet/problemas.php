					<TD VALIGN="TOP"><STRONG>8.</STRONG></TD>
					<TD><STRONG>He perdido mi clave. &iquest;Qu&eacute; hago ahora?</STRONG></TD>
				</TR>
				<TR CLASS="contenido"> 
					<TD VALIGN="TOP">R:</TD>
					<TD><P>Si no recuerdas tu clave, entonces en la p&aacute;gina inicial de la Intranet puedes pinchar 
							en &quot;&iquest;Olvidaste tu clave?&quot; y seguir los siguientes pasos:</P>
						<OL>
									<LI>Ingresa tu nombre de usuario. El nombre de usuario que se te asign&oacute; corresponde 
										a tu RUT completo. Incluido el gui&oacute;n y el d&iacute;gito despu&eacute;s del gui&oacute;n 
										(Por ejemplo 3761353-0). Luego pulsa &quot;Enviar&quot;.</LI>
							<LI>Responde la pregunta secreta que te formulaste cuando te diste de alta. Procura no tener 
								activado en el teclado la tecla &quot;May&uacute;sculas&quot;, ya que la verificaci&oacute;n 
								que realiza el programa es sensible a min&uacute;sculas y may&uacute;sculas. Luego pulsa &quot;Enviar&quot;.</LI>
							<LI>Nosotros te enviamos autom&aacute;ticamente tu clave a la cuenta de correo que proporcionaste cuando te dimos del alta en el sitio. Si no ten&iacute;as correo, entonces el programa te muestra la clave inmediatamente, sin necesidad de envi&aacute;rtela a ning&uacute;n correo.</LI>
