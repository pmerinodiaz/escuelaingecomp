					<TD VALIGN="TOP"><STRONG>6.</STRONG></TD>
					<TD><STRONG>Despu&eacute;s de ingresar mi nombre de usuario, la clave y el privilegio, se me muestra 
						un mensaje que dice &quot;No est&aacute;s registrado como usuario interno&quot;. &iquest;C&oacute;mo 
