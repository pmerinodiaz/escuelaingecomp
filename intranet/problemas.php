					<TD VALIGN="TOP"><STRONG>5.</STRONG></TD>
					<TD><P><STRONG>Tras introducir mi nombre de usuario, la clave y el privilegio, me devuelve un mensaje 
							de &quot;Tu nombre de usuario y/o clave no son v&aacute;lidas&quot;. &iquest;Qu&eacute; debo 
							hacer para que me deje entrar?</STRONG></P></TD>
				</TR>
				<TR CLASS="contenido"> 
					<TD VALIGN="TOP">R:</TD>
					<TD><P>Este problema se presenta cuando el nombre de usuario y clave que ingresaste no son los correctos. 
							Es decir, el programa no te permite entrar a la Intranet, ya que no se encontr&oacute; ning&uacute;n 
							registro con los datos que tu ingresaste. Las posibles soluciones a este problema son:</P>
								<OL>
									<LI>Vuelve a ingresar tu nombre de usuario. El nombre de usuario que se te asign&oacute; 
										corresponde a tu RUT completo. Incluido el gui&oacute;n y el d&iacute;gito despu&eacute;s 
										del gui&oacute;n. Por ejemplo 3761353-0. Vuelve a ingresar tu clave. Procura no tener 
										activado en el teclado la tecla &quot;May&uacute;sculas&quot;, ya que la verificaci&oacute;n 
										que se realiza el programa es sensible a min&uacute;sculas y may&uacute;sculas. Elige 
										el privilegio y pulsa &quot;Iniciar sesi&oacute;n&quot;. </LI>
									<LI>Pulsa &quot;&iquest;Olvide tu clave?&quot; y sigue los 3 pasos que se presentan. Con 
										los datos que te entregue el programa debes intentar ingresar siguiendo la soluci&oacute;n 
										n&ordm; 1.</LI>
									<LI>Si el problema a&uacute;n persiste, entonces puede suceder que no estas registrado en 
										nuestra Base de Datos. Para registrar tus datos, comun&iacute;cate con nosotros al correo 
										<A HREF="mailto:escuelaingecomp@gmail.com" TITLE="escuelaingecomp@gmail.com">escuelaingecomp@gmail.com</A> o bien deja tus datos 
										en la Secretar&iacute;a de la Escuela IC.</LI>
								</OL></TD>
				</TR>
				<TR> 
					<TD COLSPAN="2">&nbsp;</TD>
