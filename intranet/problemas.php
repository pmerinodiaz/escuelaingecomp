					<TD VALIGN="TOP"><STRONG>7.</STRONG></TD>
					<TD><P><STRONG>Luego de introducir mi nombre de usuario, la clave y el privilegio, me muestra un 
							mensaje de &quot;El permiso al privilegio seleccionado fue denegado&quot;. &iquest;Qu&eacute; 
							puedo hacer para que me deje entrar?</STRONG></P></TD>
				</TR>
				<TR CLASS="contenido"> 
					<TD VALIGN="TOP">R:</TD>
					<TD><P>Este problema se presenta cuando escoges un privilegio dentro del Sitio Web que en realidad 
							no tienes. Por ejemplo, si solamente eres un alumno de la Carrera IC y eliges un privilegio 
							distinto a &quot;Alumno&quot;, bien podr&iacute;a ser &quot;Acad&eacute;mico&quot;, y luego 
							inicias la sesi&oacute;n, entonces el sistema de autentificaci&oacute;n no te da permiso para 
							ingresar a la Intranet con el privilegio de &quot;Acad&eacute;mico&quot;. Como alumno de la 
							Carrera IC debes escoger el privilegio &quot;Alumno&quot; para iniciar tu sesi&oacute;n.</P>
						<P>En el caso de tener mas de un privilegio (Por ejemplo, si eres Alumno e Integrante del CEC 
							a la vez) debes escoger el privilegio con el cual tu deseas iniciar tu sesi&oacute;n.</P>
						<P>Te recomendamos que en tus primeras visitas, pruebes iniciar tu sesi&oacute;n con los diferentes 
							tipos de privilegios que se te muestran, con el fin de saber cu&aacute;les privilegios se te 
							asignaron inicialmente.</P></TD>
				</TR>
