				<TR> 
					<TD COLSPAN="2" CLASS="contenido"><B> 1. Objetivos de la Tesis<A NAME="1"></A></B></TD>
				</TR>
				<TR> 
					<TD COLSPAN="2">&nbsp;</TD>
				</TR>
				<TR> 
					<TD WIDTH="10%" CLASS="contenido" VALIGN="TOP">Art. 1</TD>
					<TD WIDTH="90%" CLASS="contenido">La Tesis o Memoria de T&iacute;tulo (MT), consiste en la realizaci&oacute;n de un trabajo de inter&eacute;s cient&iacute;fico-tecnol&oacute;gico, usando las herramientas te&oacute;rico-pr&aacute;cticas adquiridas en el desarrollo de la carrera. Se estima que, considerando una dedicaci&oacute;n completa y en la forma que est&aacute;n coordinados los plazos, deber&iacute;a poder realizarse en un a&ntilde;o a lo m&aacute;s.</TD>
				</TR>
				<TR> 
					<TD WIDTH="10%" CLASS="contenido" VALIGN="TOP">Art. 2</TD>
					<TD WIDTH="90%" CLASS="contenido">La MT deber&aacute; tener un nivel compatible con el t&iacute;tulo al cual se opta, adem&aacute;s deber&aacute; significar en alguna medida un aporte a la disciplina en la cual se desarrolle y deber&aacute; permitir medir la capacidad del alumno para realizar y comunicar un trabajo personal con objetivos y plazos previamente definidos.</TD>
				</TR>
				<TR> 
					<TD WIDTH="10%" CLASS="contenido" VALIGN="TOP">Art.3</TD>
					<TD WIDTH="90%" CLASS="contenido">
						<P>La MT considera diversas instancias que derivan en la evaluaci&oacute;n final del mismo, ellas son:</P>
						<UL>
							<LI>Solicitar e inscribir el tema de memoria. </LI>
							<LI>Aceptaci&oacute;n del tema de memoria.</LI>
							<LI>Control de Avance.</LI>
							<LI>Informe Final.</LI>
							<LI>Defensa Memoria de T&iacute;tulo.</LI>
						</UL>
					</TD>
				</TR>
				<TR> 
					<TD COLSPAN="2">&nbsp;</TD>
				</TR>
				<TR> 
					<TD COLSPAN="2" CLASS="contenido"><B>2. Inscripci&oacute;n del tema<A NAME="2"></A></B></TD>
				</TR>
				<TR> 
					<TD COLSPAN="2">&nbsp;</TD>
				</TR>
				<TR> 
					<TD WIDTH="10%" CLASS="contenido" VALIGN="TOP">Art. 1</TD>
					<TD WIDTH="90%" CLASS="contenido">El alumno tendr&aacute; el derecho a solicitar la inscripci&oacute;n de un tema de memoria para cuando haya cursado el 9&ordm; Nivel. El tema de memoria deber&aacute; estar fundamentado en las asignaturas del Plan de Estudio de la Carrera de Ingenier&iacute;a en Computaci&oacute;n.</TD>
				</TR>
				<TR> 
					<TD WIDTH="10%" CLASS="contenido" VALIGN="TOP">Art. 2</TD>
					<TD WIDTH="90%" CLASS="contenido">El tema de memoria deber&aacute; ser patrocinado por un acad&eacute;mico jornada completa del Area de Computaci&oacute;n, publicado y dado a conocer a todos los profesores del Area de Computaci&oacute;n. Luego de la publicaci&oacute;n, y al cabo de dos semanas tras las observaciones que pudieran llegar de los distintos integrantes, una Comisi&oacute;n de Selecci&oacute;n del Area de Computaci&oacute;n entregara un informe que, la composici&oacute;n de esta Comisi&oacute;n queda a criterio del Director de Escuela, la que considerando la originalidad, la viabilidad del tema, y los argumentos que sustentan la propuesta entregar&aacute; una resoluci&oacute;n, la que ser&aacute; de Aprobada o Rechazada. De acuerdo a la din&aacute;mica del proceso, la Comisi&oacute;n de Selecci&oacute;n se reserva el derecho de fijar fechas de recepci&oacute;n de anteproyectos.</TD>
				</TR>
				<TR> 
					<TD WIDTH="10%" CLASS="contenido" VALIGN="TOP">Art. 3</TD>
					<TD WIDTH="90%" CLASS="contenido">Inmediatamente en caso de aprobarse el tema, el profesor patrocinante pasa a ser profesor gu&iacute;a para los efectos de este reglamento. En caso de temas interdisciplinarios, es posible incorporar a un profesor correferente externo al Area de Computaci&oacute;n.</TD>
				</TR>
				<TR> 
					<TD WIDTH="10%" CLASS="contenido" VALIGN="TOP">Art. 4</TD>
					<TD WIDTH="90%" CLASS="contenido">El Director de Escuela deber&aacute; mantener una lista actualizada de los temas aprobados a disposici&oacute;n, con el fin de evitar repeticiones, as&iacute; como del seguimiento del futuro profesional, por los mecanismos que estime conveniente.</TD>
				</TR>
				<TR> 
					<TD COLSPAN="2">&nbsp;</TD>
				</TR>
				<TR> 
					<TD COLSPAN="2" CLASS="contenido"><B>3. Plazos<A NAME="3"></A></B></TD>
				</TR>
				<TR> 
					<TD COLSPAN="2">&nbsp;</TD>
				</TR>
				<TR> 
					<TD WIDTH="10%" CLASS="contenido" VALIGN="TOP">Art. 1</TD>
					<TD WIDTH="90%" CLASS="contenido">Dentro del plazo normal se deber&aacute;n definir 2 fechas claves: Control Avance (no superior a 5 meses desde la fecha de aceptaci&oacute;n del tema), y Entrega Informe Final (no superior a 10 meses desde la fecha de aprobaci&oacute;n del tema). Sin perjuicio de lo anterior podr&aacute;n existir otros Informes o Controles de Avance antes de cumplirse el plazo, pero no deber&iacute;an llamarse Informe Final. Con la entrega de este trabajo se procede a evaluar la Memoria de T&iacute;tulo.</TD>
				</TR>
				<TR> 
					<TD WIDTH="10%" CLASS="contenido" VALIGN="TOP">Art. 2</TD>
					<TD WIDTH="90%" CLASS="contenido">El no cumplimiento de estos plazos y fechas claves, significa la reprobaci&oacute;n de la Memoria de T&iacute;tulo.</TD>
				</TR>
				<TR> 
					<TD COLSPAN="2" ALIGN="CENTER"><IMG SRC="activos/plazos.gif" WIDTH="440" HEIGHT="145"></TD>
				</TR>
