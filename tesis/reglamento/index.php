				</TR>
				<TR> 
					<TD COLSPAN="2" CLASS="contenido"><B>4. Seguimiento<A NAME="4"></A></B></TD>
				</TR>
				<TR> 
					<TD COLSPAN="2">&nbsp;</TD>
				</TR>
				<TR> 
					<TD WIDTH="10%" CLASS="contenido" VALIGN="TOP">Art. 1</TD>
					<TD WIDTH="90%" CLASS="contenido">La Direcci&oacute;n de Escuela velar&aacute; por el cumplimiento de plazos y fechas claves.</TD>
				</TR>
				<TR> 
					<TD WIDTH="10%" CLASS="contenido" VALIGN="TOP">Art. 2</TD>
					<TD WIDTH="90%" CLASS="contenido">Los temas que no cumplan con los plazos acordados ser&aacute;n liberados y podr&aacute;n ser publicados como temas para nuevas postulaciones.</TD>
				</TR>
				<TR> 
					<TD COLSPAN="2">&nbsp;</TD>
				</TR>
				<TR> 
					<TD COLSPAN="2" CLASS="contenido"><B>5. Comisi&oacute;n examinadora<A NAME="5"></A></B></TD>
				</TR>
				<TR> 
					<TD COLSPAN="2">&nbsp;</TD>
				</TR>
				<TR> 
					<TD WIDTH="10%" CLASS="contenido" VALIGN="TOP">Art. 1</TD>
					<TD WIDTH="90%" CLASS="contenido">La Comisi&oacute;n Examinadora (CE) es la comisi&oacute;n que eval&uacute;a la Memoria de T&iacute;tulo, la que esta integrada por 3 miembros, profesor gu&iacute;a, profesor correferente, (en caso de existir), adem&aacute;s de integrantes del Consejo de Escuela, distinto del profesor gu&iacute;a.</TD>
				</TR>
				<TR> 
					<TD WIDTH="10%" CLASS="contenido" VALIGN="TOP">Art. 2</TD>
					<TD WIDTH="90%" CLASS="contenido">La CE ser&aacute; presidida y convocada por el Director de Escuela de acuerdo a los plazos y fechas claves, la que tendr&aacute; un plazo m&aacute;ximo de 2 Meses para su evaluaci&oacute;n, contados desde la fecha de entrega del informe final (escrito, seg&uacute;n el formato (ver Anexo)), al t&eacute;rmino del cual la CE har&aacute; llegar a la Direcci&oacute;n de la Escuela un informe que argumenta la nota que obtuvo el (o los) alumno(s).</TD>
				</TR>
				<TR> 
					<TD WIDTH="10%" CLASS="contenido" VALIGN="TOP">Art. 3</TD>
					<TD WIDTH="90%" CLASS="contenido">El alumno podr&aacute; presentarse a la defensa de memoria de t&iacute;tulo siempre que el informe final (escrito) de su memoria haya sido aceptado por la CE.</TD>
				</TR>
				<TR> 
					<TD WIDTH="10%" CLASS="contenido" VALIGN="TOP">Art. 4</TD>
					<TD WIDTH="90%" CLASS="contenido">La defensa de Memoria de T&iacute;tulo es la etapa con que se culmina el proceso de Memoria de Titulo y que consiste en la defensa oral del tema, el cual ser&aacute; oral y p&uacute;blico (excepcionalmente la CE podr&aacute; calificarlo en privado). La fecha de la defensa de Memoria de T&iacute;tulo debe ser fijada por lo menos con dos semanas de anticipaci&oacute;n, para el env&iacute;o de las invitaciones pertinentes.</TD>
				</TR>
				<TR> 
					<TD WIDTH="10%" CLASS="contenido" VALIGN="TOP">Art. 5</TD>
					<TD WIDTH="90%" CLASS="contenido">La defensa (oral) de Memoria de T&iacute;tulo se har&aacute; frente a la CE, siendo el Director de Escuela de Ingenier&iacute;a en Computaci&oacute;n, qui&eacute;n la preside. La defensa (oral) tendr&aacute; un tiempo m&aacute;ximo de 45 minutos, al t&eacute;rmino del cual la CE tendr&aacute; a lo m&aacute;s 1 hora, para realizar preguntas, deliberar y asignar la evaluaci&oacute;n que corresponda. El presidente de esta CE se remitir&aacute; a leer la evaluaci&oacute;n, seg&uacute;n los considerandos que la CE estime conveniente.</TD>
				</TR>
				<TR> 
					<TD COLSPAN="2">&nbsp;</TD>
				</TR>
				<TR> 
					<TD COLSPAN="2" CLASS="contenido"><B>6. Evaluaci&oacute;n<A NAME="6"></A></B></TD>
				</TR>
				<TR> 
					<TD COLSPAN="2">&nbsp;</TD>
				</TR>
				<TR> 
					<TD WIDTH="10%" CLASS="contenido" VALIGN="TOP">Art. 1</TD>
					<TD WIDTH="90%" CLASS="contenido">
						<P>La CE calificar&aacute; la Memoria de Titulo con una nota en la escala de 1 a 7, considerando las siguientes ponderaciones:</P>
						<UL>
							<LI>Informe Final: 50%.</LI>
							<LI>Defensa Memoria de Titulo: 50%.</LI>
						</UL>
					</TD>
				</TR>
				<TR> 
					<TD WIDTH="10%" CLASS="contenido" VALIGN="TOP">Art. 2</TD>
					<TD WIDTH="90%" CLASS="contenido">La nota m&iacute;nima de aprobaci&oacute;n en cada uno de los hitos debe ser (&gt;=4.0). En caso de una nota inferior a &eacute;sta, el alumno reprobar&aacute; la Memoria de Titulo, teniendo derecho a una segunda y &uacute;nica oportunidad para inscribir y desarrollar un nuevo tema de memoria.</TD>
				</TR>
				<TR> 
					<TD WIDTH="10%" CLASS="contenido" VALIGN="TOP">Art. 3</TD>
					<TD WIDTH="90%" CLASS="contenido">El acta de aprobaci&oacute;n de la Memoria de Titulo ser&aacute; firmada por el Director de Escuela, s&oacute;lo si no existen deudas pendientes con el Departamento o alguna otra unidad interna de la Universidad (l&eacute;ase libros, copias memorias, CD's, etc.).</TD>
				</TR>
