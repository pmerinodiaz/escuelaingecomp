				<TR> 
					<TD COLSPAN="2" CLASS="contenido"><B>7. Casos especiales<A NAME="7"></A></B></TD>
				</TR>
				<TR> 
					<TD COLSPAN="2">&nbsp;</TD>
				</TR>
